import DOMPurify from 'isomorphic-dompurify';

export function sanitizeHtml(html: string): string {
    return DOMPurify.sanitize(html, {
        ALLOWED_TAGS: [
            'a',
            'abbr',
            'acronym',
            'address',
            'article',
            'aside',
            'b',
            'bdi',
            'bdo',
            'big',
            'blockquote',
            'body',
            'br',
            'caption',
            'center',
            'cite',
            'code',
            'col',
            'colgroup',
            'dd',
            'del',
            'details',
            'dfn',
            'div',
            'dl',
            'dt',
            'em',
            'figcaption',
            'figure',
            'footer',
            'h1',
            'h2',
            'h3',
            'h4',
            'h5',
            'h6',
            'head',
            'header',
            'hr',
            'i',
            'img',
            'ins',
            'kbd',
            'li',
            'main',
            'mark',
            'nav',
            'ol',
            'p',
            'pre',
            'q',
            's',
            'samp',
            'section',
            'small',
            'span',
            'strike',
            'strong',
            'sub',
            'summary',
            'sup',
            'table',
            'tbody',
            'td',
            'tfoot',
            'th',
            'thead',
            'time',
            'title',
            'tr',
            'tt',
            'u',
            'ul',
            'wbr',
        ],
        ALLOWED_ATTR: [
            'href',
            'title',
            'target',
            'rel',
            'src',
            'alt',
            'width',
            'height',
            'class',
            'id',
            'style',
            'dir',
            'lang',
            'role',
            'tabindex',
            'aria-label',
            'aria-describedby',
            'aria-hidden',
            'aria-expanded',
            'aria-controls',
            'aria-labelledby',
            'datetime',
            'colspan',
            'rowspan',
            'data-*',
        ],
        ALLOW_DATA_ATTR: true,
    });
}
